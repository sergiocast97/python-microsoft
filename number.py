# Type conversion

# String conversion
age = input('What is your age?: ')
age_string = str(age)
print('Ah yes, ' + age_string)

# Int Conversion
year_string = input('What year were you born?: ')
year = int(year_string)
print('Ah yes, ' + str(2020 - year) + ' before 2020' )

# Float Conversion
height_string = input('What is your height?: ')
height = float(height_string)
print('Ah yes, ' + str(2.0 - height) + ' less than 2 meters')